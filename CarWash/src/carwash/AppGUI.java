    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        vehicle = vehicleTypeTf.getText();
        reg = regTf.getText();
        washType = washTypeTf.getText();
        
        if(vehicle.equals("") || reg.equals("") || washType.equals("") ){
            outputTa.append("Error, all fields must be filled in!!" +"\n");
        }
        else{
            //Store text field inouts in local string variables
        

        //create instance of vehicle class and pass in required parameters
        v = new Vehicle(vehicle, reg, washType);
        v.setVehicle(vehicle);
        v.setReg(reg);
        v.setWashType(washType);

        //call my queue (q) and add the vehicle object to it
        q.enqueue(v);
        outputTa.append("A " + vehicle + " was added to the queue " + " with registration " + reg + " with wash option " + washType + "\n");

        //clearing text fields for more inputs
        vehicleTypeTf.setText("");
        regTf.setText("");
        washTypeTf.setText("");
        }
        
    }//GEN-LAST:event_addBtnActionPerformed

