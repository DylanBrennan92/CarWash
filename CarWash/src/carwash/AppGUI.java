    private void finishedBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishedBtnActionPerformed

        //creating a removedVehicle object and setting it as a "clone" of the object which is being dequeued, so I can print it back through the GUI
        String vType;
        String vReg;
        String vWashType;

        //If statement to handle a scenario whereby the queue might be empty - to stop the program from crashing
        if (!(q.isEmpty())) {
            Vehicle removedVehicle = q.dequeue();
            vType = removedVehicle.getVehicle();
            vReg = removedVehicle.getReg();
            vWashType = removedVehicle.getWashType();
            outputTa.append("Finished washing " + vType + " with registration " + vReg + " with wash type " + vWashType + "\n");
        } else {
            outputTa.append("No vehicles currently in the queue to remove" + "\n");
        }
    }//GEN-LAST:event_finishedBtnActionPerformed

    private void nextBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextBtnActionPerformed
        String vType;
        String vReg;
        String vWashType;
        if (!(q.isEmpty())) {
        Vehicle nextV = q.frontElement();
        vType = nextV.getVehicle();
        vReg = nextV.getReg();
        vWashType = nextV.getWashType();
        
       outputTa.append("Next vehicle to wash is " + vType + " with registration " + vReg + " with wash type " + vWashType + "\n");
        }
        else{
            outputTa.append("No vehicles currently in the queue, Take a break" + "\n");
        }
        
    }//GEN-LAST:event_nextBtnActionPerformed

